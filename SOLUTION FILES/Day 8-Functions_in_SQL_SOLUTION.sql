-- VIEW HIGH SALARIED EMPLOYEES
CREATE FUNCTION DBO.HIGHINFO_ABOVELIMIT(@AMOUNT DECIMAL(10,2))
RETURNS TABLE AS 
RETURN
(
SELECT * FROM EmployeeDATA WHERE Salary>@AMOUNT
);

-- CALL TABLE VALUED FUNCTION
SELECT * FROM DBO.HIGHINFO_ABOVELIMIT(50000);

-- HIGH BONUS EMPLOYEES
CREATE FUNCTION DBO.HIGHINFO_ABOVELIMIT2(@AMOUNT DECIMAL(10,2))
RETURNS TABLE AS 
RETURN
(
SELECT * FROM EmployeeDATA WHERE BONUS>=@AMOUNT
);

-- CALL TABLE VALUED FUNCTION
SELECT * FROM DBO.HIGHINFO_ABOVELIMIT2(5000);

-- CALCULATE NET SALARY DEPARTMENT WISE 
CREATE FUNCTION DBO.DEPARTWISE_NETSAL(@DEPART VARCHAR(50))
RETURNS TABLE
AS RETURN (SELECT SUM(NETSALARY) AS TOTAL_NET_SALARY FROM EmployeeDATA
WHERE Department=@DEPART);

-- CALL THE FUNCTION AND VIEW THE DATA
SELECT * FROM DBO.DEPARTWISE_NETSAL('HR');