-- A FUNCTION THAT TAKES 2 INPUTS, ADDS THEM AND RETURNS THE SUM AS OUTPUT
CREATE FUNCTION DBO.SUM_2IPS(@INPUT1 DECIMAL(10,2),@INPUT2 DECIMAL(10,2))
RETURNS DECIMAL(10,2)
AS BEGIN
    DECLARE @RESULT DECIMAL(10,2); -- DECLARING VARIABLE NAME ALONG WITH ITS DATATYPE
    SET @RESULT = @INPUT1 + @INPUT2; -- INITIALIZING VARIABLE WITH INPUT1+INPUT2
    RETURN @RESULT;
END;
-- TO CALL A FUNCTION --> SELECT DBO.FUNCTIONNAME(PARAMETERS);
SELECT DBO.SUM_2IPS(SALARY,BONUS) AS NETSALARY FROM EmployeeDATA;
SELECT * FROM EmployeeDATA;
-- UPDATE THIS INFORMATION IN THE TABLE
UPDATE EmployeeDATA
SET NETSALARY= DBO.SUM_2IPS(SALARY,BONUS);

