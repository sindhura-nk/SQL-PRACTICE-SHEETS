-- TRIGGERS
/*
Syntax
CREATE TRIGGER TriggerName ON Table AFTER INSERT|UPDATE|DELETE AS BEGIN ... END
Use
Automatically perform actions after an event.

Send an alert when a new employee is added.

TRIGGERS ARE USED BY DBA,DEVELOPERS..NOT BY ANALYSTS
TRIGGERS ARE IMPLEMENTED ONLY FOR DML OPERATIONS
*/
USE CX_180_INDEXES;

CREATE TABLE AUDIT_EMPLOYEE_INSERT
(
AUDIT_ID INT IDENTITY(1,1), -- IDENTITY WORKS AS AUTOINCREMENT
AUDIT_MESSAGE VARCHAR(MAX),
LOG_INFO DATETIME DEFAULT GETDATE()
);


CREATE TRIGGER  TRG_RECORD ON EMPLOYEES
AFTER INSERT AS
BEGIN
INSERT INTO AUDIT_EMPLOYEE_INSERT (AUDIT_MESSAGE,LOG_INFO) VALUES ('NEW EMPLOYEE RECORD INSERTED',DEFAULT);
END;


